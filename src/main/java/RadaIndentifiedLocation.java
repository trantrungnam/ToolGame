public class RadaIndentifiedLocation {
    private Integer npcPointX;
    private Integer npcPointY;
    private String nameNPC;
    private String regionName;
    private Integer moveAtX;
    private Integer moveAtY;

    public Integer getNpcPointX() {
        return npcPointX;
    }

    public void setNpcPointX(Integer npcPointX) {
        this.npcPointX = npcPointX;
    }

    public Integer getNpcPointY() {
        return npcPointY;
    }

    public void setNpcPointY(Integer npcPointY) {
        this.npcPointY = npcPointY;
    }

    public String getNameNPC() {
        return nameNPC;
    }

    public void setNameNPC(String nameNPC) {
        this.nameNPC = nameNPC;
    }

    public String getRegionName() {
        return regionName;
    }

    public void setRegionName(String regionName) {
        this.regionName = regionName;
    }

    public Integer getMoveAtX() {
        return moveAtX;
    }

    public void setMoveAtX(Integer moveAtX) {
        this.moveAtX = moveAtX;
    }

    public Integer getMoveAtY() {
        return moveAtY;
    }

    public void setMoveAtY(Integer moveAtY) {
        this.moveAtY = moveAtY;
    }
}
